package enviromine.trackers;

public class RotProperties
{
	public int id;
	public int meta;
	public int rotID;
	public int rotMeta;
	public double days;
	
	public RotProperties(int id, int meta, int rotID, int rotMeta, double days)
	{
		this.id = id;
		this.meta = meta;
		this.rotID = rotID;
		this.rotMeta = rotMeta;
		this.days = days;
	}
}
